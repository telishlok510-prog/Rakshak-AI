interface I18nContextValue {
  lang: LanguageCode;
  setLang: (l: LanguageCode) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LanguageCode>("en");

  useEffect(() => {
    const saved = window.localStorage.getItem("srp-lang") as LanguageCode | null;
    if (saved && STRINGS[saved]) setLangState(saved);
  }, []);

  // Keep <html lang="..."> in sync so CSS can target Gujarati specifically
  // (e.g. looser heading line-height — Gujarati matras need more vertical
  // room than Latin script) and screen readers announce the right language.
  useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  const setLang = (l: LanguageCode) => {
    setLangState(l);
    window.localStorage.setItem("srp-lang", l);
  };

  const t = (key: string) => STRINGS[lang][key] ?? STRINGS.en[key] ?? key;

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
